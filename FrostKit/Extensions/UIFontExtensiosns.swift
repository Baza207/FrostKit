import UIKit

extension UIFont {
    
    public class func fontAwesome(#size: CGFloat) -> UIFont {
        return UIFont(name: "FontAwesome", size: size)
    }
    
    public class func ionicons(#size: CGFloat) -> UIFont {
        return UIFont(name: "Ionicons", size: size)
    }
    
}
